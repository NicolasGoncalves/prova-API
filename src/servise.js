
export function somar (j, n){
    return n + j;
}

export function dobro (n){
    return n * 2;
}

export function corprimaria(cor){

    let img = "";

    if((cor == "azul") ||(cor == "amarelo") || (cor =="vermelho")) {
        img = "true";
    }
    else(
        img="false;"
    )
    return img;
}